	var result = new VideoDefinition();

	// download webpage
	var http = new Http();
	var html = http.downloadWebpage(url);

	// get video title
	var tmp = copyBetween(html, '<h2 class="videoTitle">','</h2>');
	result.title = copyBetween(tmp, '<span>', '</span');

	// get the video  url
	var configUrl = copyBetween(html, "so.addVariable('config', '", "');");
	var config    = http.downloadWebpage("http://www.porntube.com" + configUrl);
	var videoUrl  = copyBetween(config, '<file>', '</file>');

	result.URL=cleanUrl(videoUrl);

	// return the video information
