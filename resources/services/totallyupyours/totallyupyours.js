function getVideoInformation(url)
{
	// video information
	var result = new VideoDefinition();

	// download webpage
	var http = new Http();
	var html = http.downloadWebpage(url);

	// get video title
        var tmp = copyBetween(html, '<div id="content">', '<div id="media">');
	result.title = copyBetween(tmp, '<h1>', '</h1>');

	// get the video  url
	var videoUrl = copyBetween(html, "?vid=", "'");

	result.URL=cleanUrl(videoUrl);

	// return the video information
	return result;
}

