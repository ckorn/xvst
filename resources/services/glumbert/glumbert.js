function getVideoInformation(url)
{
	const URL_GET_FLV = "http://cdn.glumbert.com/video/flash/%1.flv";
	// video information
	var result = new VideoDefinition();
	// download webpage
	var http = new Http();
	var html = http.downloadWebpage(url);
	// get video info
	result.title = copyBetween(html, "var NAME = \"", "\"");
	result.URL = copyBetween(html, "var URLNAME = \"", "\"");
	// clear and get the final url
	result.URL = cleanUrl(strFormat(URL_GET_FLV, result.URL));
	// return the video information
	return result;
}

