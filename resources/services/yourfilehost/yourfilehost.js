function getVideoInformation(url)
{
	// video information
	var result = new VideoDefinition();
	// download html
	var http = new Http();
	var html = http.downloadWebpage(url);
	// get video title
	result.title = copyBetween(html, "Description:", "<br>");
	// get xml url
	var xml_url = cleanUrl(copyBetween(html, "&video=", "&"));
	// download xml
	var xml = http.downloadWebpage(xml_url);
	// get flv url
	result.URL = cleanUrl(copyBetween(xml, "video_id=", "&"));
	// get the stored cookies
	result.cookies = http.getCookies("|");
	// return the video information
	return result;
}

